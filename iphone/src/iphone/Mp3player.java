package iphone; // 演習04 (継承)

public class Mp3player {
	
	//演習①
	public void play() {
		System.out.println("再生");
	}
	public void stop() {
		System.out.println("停止");
	}
	public void next() {
		System.out.println("次へ");
	}
	public void back() {
		System.out.println("戻る");
	}
	
	
	//演習⑤ (Android用)
	public void music() {
		System.out.println("音楽");
	}
}
